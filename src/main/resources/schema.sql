create table if not exists family
(
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(50),
    ADDRESS VARCHAR(100)


);

create table if not exists person
(
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FIRSTNAME VARCHAR(15),
    LASTNAME VARCHAR(15),
    AGE INT,
    FAMILY_ID INT,
    IS_MARRIED BOOLEAN,
    FOREIGN KEY (FAMILY_ID) REFERENCES FAMILY(ID)

);
